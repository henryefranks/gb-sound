def gen_tile(data):
    res = "dw "

    for row in data:
        lsb = ""
        msb = ""

        for tile in row:
            if tile == "0":
                lsb += "0"
                msb += "0"
            elif tile == "1":
                lsb += "1"
                msb += "0"
            elif tile == "2":
                lsb += "0"
                msb += "1"
            elif tile == "3":
                lsb += "1"
                msb += "1"
            else:
                raise Exception(f"Invalid Character: {tile}")
        
        res += f"${int(lsb, 2):02x}{int(msb, 2):02x}, "

    return res[:-2]


